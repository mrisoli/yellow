function getStatusColor(isLoading: boolean, data: string | undefined) {
	if (data === "OK") {
		return "bg-green-500";
	}
	if (isLoading) {
		return "bg-orange-400";
	}
	return "bg-red-500";
}

function getStatusText(isLoading: boolean, data: string | undefined) {
	if (isLoading) {
		return "Checking...";
	}
	if (data === "OK") {
		return "Connected";
	}
	return "Error";
}

function HomeComponent() {
	const healthCheck = useQuery(convexQuery(api.healthCheck.get, {}));

	return (
		<div className="container mx-auto max-w-3xl px-4 py-2">
			<pre className="overflow-x-auto font-mono text-sm">{TITLE_TEXT}</pre>
			<div className="grid gap-6">
				<section className="rounded-lg border p-4">
					<h2 className="mb-2 font-medium">API Status</h2>
					<div className="flex items-center gap-2">
						<div
							className={`h-2 w-2 rounded-full ${getStatusColor(healthCheck.isLoading, healthCheck.data)}`}
						/>
						<span className="text-muted-foreground text-sm">
							{getStatusText(healthCheck.isLoading, healthCheck.data)}
						</span>
					</div>
				</section>
			</div>
		</div>
	);
}
